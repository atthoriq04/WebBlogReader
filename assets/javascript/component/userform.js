function accountEdit(username, _name) {
  const back = document.createElement("a");
  back.href = "";
  back.id = "toAccount";
  back.innerHTML = "Back";
  const form = document.createElement("form");
  form.method = "POST";
  form.id = "accountEdit";
  form.action = "../data/config/user.php";
  const user = document.createElement("input");
  user.type = "text";
  user.name = "uName";
  user.id = "username";
  user.value = username;
  const userLabel = document.createElement("label");
  userLabel.htmlFor = "username";
  userLabel.appendChild(document.createTextNode("Username"));
  const name = document.createElement("input");
  name.type = "text";
  name.name = "name";
  name.id = "name";
  name.value = _name;
  const nameLabel = document.createElement("label");
  nameLabel.htmlFor = "name";
  nameLabel.appendChild(document.createTextNode("Name"));
  const button = document.createElement("button");
  button.type = "Submit";
  button.classList = "submitEdit";
  button.name = "edit";
  const text = document.createTextNode("Edit your Account");
  button.appendChild(text);
  const link = document.createElement("a");
  link.href = "";
  link.id = "cPassword";
  link.innerHTML = "Change Password?";
  form.appendChild(back);
  form.appendChild(userLabel);
  form.appendChild(user);
  form.appendChild(nameLabel);
  form.appendChild(name);
  form.appendChild(button);
  form.appendChild(link);
  return form;
}

