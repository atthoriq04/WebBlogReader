function registerForm() {
  const form = document.createElement("form");
  form.method = "POST";
  form.id = "fRegister";
  form.classList.add("form");
  form.action = "../data/config/user.php";
  const username = document.createElement("input");
  username.type = "text";
  username.name = "Rusername";
  username.id = "username";
  const userLabel = document.createElement("label");
  userLabel.htmlFor = "username";
  userLabel.appendChild(document.createTextNode("Add your Username"));
  const password = document.createElement("input");
  password.type = "password";
  password.name = "Rpassword";
  password.id = "password";
  const passLabel = document.createElement("label");
  passLabel.htmlFor = "password";
  passLabel.appendChild(document.createTextNode("Add Your Password"));
  const passwordR = document.createElement("input");
  passwordR.type = "password";
  passwordR.name = "pRepeat";
  passwordR.id = "pRepeat";
  const rLabel = document.createElement("label");
  rLabel.htmlFor = "pRepeat";
  rLabel.appendChild(document.createTextNode("Repeat Your Password"));
  const button = document.createElement("button");
  button.type = "Submit";
  button.classList = "registerButton";
  button.name = "register";
  const text = document.createTextNode("Register your Account");
  button.appendChild(text);
  const reload = document.createElement("a");
  reload.href = "ww";
  reload.classList.add("reload");
  reload.appendChild(
    document.createTextNode("Already have an Account? Log In")
  );
  const p1 = document.createElement("p");
  p1.id = "validateUser";
  p1.classList.add("validate");
  const p2 = document.createElement("p");
  p2.id = "validatePass";
  p2.classList.add("validate");
  form.appendChild(userLabel);
  form.appendChild(username);
  form.appendChild(p1);
  form.appendChild(passLabel);
  form.appendChild(password);
  form.appendChild(rLabel);
  form.appendChild(passwordR);
  form.appendChild(p2);
  form.appendChild(button);
  form.appendChild(reload);
  return form;
}

